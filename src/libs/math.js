/** Suma de dos Numeros
 * @param {number} x Primer numero
 * @param {number} y Segundo Numero
 * @returns {number} La suma de x y 
 * 
 */
const add = (x,y) => x + y;



/** Resta de dos Numeros
 * @param {number} x Primer numero
 * @param {number} y Segundo Numero
 * @returns {number} La resta de x y 
 * 
 */
const substract = (x,y) => x - y;